package com.icog.jobs.applications.enums;

public enum ApplicationStatus {
    SUBMITTED,              // Application submitted and received
    SCREENING,              // Initial screening of qualifications
    INTERVIEW_SCHEDULED,    // Interview has been scheduled
    INTERVIEW_COMPLETED,    // Interview has been completed
    SHORTLISTED,            // Shortlisted for the next steps
    OFFER_EXTENDED,         // Job offer has been extended
    OFFER_ACCEPTED,         // Offer has been accepted
    REJECTED,               // Application was not successful
}

